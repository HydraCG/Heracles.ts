/**
 * Provides possible {@link IHypermediaProcessor} support levels.
 */
export enum Level {
  /**
   * @enum
   * @type {number}
   * Defines a not supported response.
   */
  None = 0,

  /**
   * @enum
   * @type {number}
   * Defines an exact support of the response.
   */
  FullSupport = 100
}
